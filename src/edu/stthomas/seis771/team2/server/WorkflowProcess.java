package edu.stthomas.seis771.team2.server;

import java.util.Date;

import edu.stthomas.seis771.team2.domainmodel.Employee;

public class WorkflowProcess 
{
	
	private int processId;
	
	
	private Employee employee;
	
	private Date requestedDate;
	
	private Date lastModifiedDate;
	
	
	public WorkflowProcess(int _procesId, Employee _emp,  Date _reqDate, Date _lmd )
	{
		this.setEmployee(_emp);
		this.setProcessId(_procesId);
		this.setRequestedDate(_reqDate);
		this.setLastModifiedDate(_lmd);
	}

	public int getProcessId() {
		return processId;
	}

	public void setProcessId(int processId) {
		this.processId = processId;
	}

	public Employee getEmployee() {
		return employee;
	}

	public void setEmployee(Employee employee) {
		this.employee = employee;
	}

	public Date getRequestedDate() {
		return requestedDate;
	}

	public void setRequestedDate(Date requestedDate) {
		this.requestedDate = requestedDate;
	}

	public Date getLastModifiedDate() {
		return lastModifiedDate;
	}

	public void setLastModifiedDate(Date lastModifiedDate) {
		this.lastModifiedDate = lastModifiedDate;
	}
 
	
	

}
